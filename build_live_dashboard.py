def _build_monthly_tab(signals: list, ai_insight: str, year: int, month: int) -> str:
    if not signals:
        return '<div style="text-align:center;padding:40px;color:#adb5bd">최근 30일 수집 데이터 없음</div>'

    reds    = [s for s in signals if s.action_flag == "red"]
    yellows = [s for s in signals if s.action_flag == "yellow"]
    whites  = [s for s in signals if s.action_flag == "white"]
    health  = max(0, 100 - len(reds)*20 - len(yellows)*5)
    hcolor  = "#27ae60" if health >= 80 else "#f39c12" if health >= 60 else "#e74c3c"

    # 시그널 유형별 집계
    by_type: dict = defaultdict(int)
    for s in signals:
        by_type[s.signal_type] += 1
    type_bars = ""
    max_cnt = max(by_type.values()) if by_type else 1
    for sig_type, cnt in sorted(by_type.items(), key=lambda x: -x[1])[:8]:
        tc, bg = _SIGNAL_TYPE_COLORS.get(sig_type, ("#455a64","#f5f5f5"))
        pct = int(cnt / max_cnt * 100)
        type_bars += f"""
        <div style="display:flex;align-items:center;gap:10px;margin-bottom:8px">
          <div style="width:130px;font-size:12px;color:#495057;flex-shrink:0">{sig_type}</div>
          <div style="flex:1;background:#f0f0f0;border-radius:4px;height:14px">
            <div style="width:{pct}%;background:{tc};border-radius:4px;height:14px"></div>
          </div>
          <div style="width:30px;text-align:right;font-size:12px;font-weight:700;color:{tc}">{cnt}</div>
        </div>"""

    # 회사별 월간 집계
    by_co: dict = defaultdict(list)
    for s in signals:
        by_co[s.portfolio_name].append(s)
    co_rows = ""
    for co, arts in sorted(by_co.items(),
                           key=lambda kv: -sum(1 for s in kv[1] if s.action_flag=="red")):
        r = sum(1 for s in arts if s.action_flag=="red")
        y = sum(1 for s in arts if s.action_flag=="yellow")
        w = sum(1 for s in arts if s.action_flag=="white")
        bg = "#fff5f5" if r else "#fffdf0" if y else "#fff"
        top = sorted(arts, key=lambda x: {"red":0,"yellow":1,"white":2}[x.action_flag])[0]
        co_rows += f"""
        <tr style="background:{bg};border-bottom:1px solid #f0f0f0">
          <td style="padding:11px 16px;font-weight:700;color:#1a1a2e">{co}</td>
          <td style="padding:11px;text-align:center">
            {'<span style="color:#c0392b;font-weight:800">'+str(r)+'</span>' if r else '—'}
          </td>
          <td style="padding:11px;text-align:center">
            {'<span style="color:#e67e22;font-weight:700">'+str(y)+'</span>' if y else '—'}
          </td>
          <td style="padding:11px;text-align:center;color:#868e96">{w}</td>
          <td style="padding:11px;font-size:12px;color:#555">{top.signal_type}</td>
          <td style="padding:11px;font-size:12px;color:#666">{(top.summary_ko or '')[:60]}…</td>
        </tr>"""

    return f"""
<!-- 월간 건전성 지수 -->
<div style="display:flex;align-items:center;gap:16px;
            background:#fff;border-radius:12px;padding:18px 24px;
            box-shadow:0 2px 8px rgba(0,0,0,.06);margin-bottom:16px">
  <div style="font-size:48px;font-weight:900;color:{hcolor};line-height:1">{health}</div>
  <div>
    <div style="font-size:11px;color:#adb5bd;font-weight:600;letter-spacing:.5px">
      포트폴리오 점수 · {year}년 {month}월
    </div>
    <div style="font-size:14px;font-weight:700;color:{hcolor};margin-top:3px">
      {'양호 (Green)' if health>=80 else '주의 (Yellow)' if health>=60 else '위험 (Red)'}
    </div>
    <div style="font-size:11px;color:#adb5bd;margin-top:2px">
      산식: 100 − (🔴×20) − (🟡×5) &nbsp;|&nbsp; 🔴 {len(reds)}건 · 🟡 {len(yellows)}건 · ⚪ {len(whites)}건
    </div>
  </div>
  <div style="margin-left:auto;text-align:right">
    <div style="font-size:11px;color:#adb5bd">총 시그널</div>
    <div style="font-size:28px;font-weight:800;color:#1a1a2e">{len(signals)}</div>
  </div>
</div>

{_ai_insight_box(ai_insight, "🤖 월간 심층 분석 (Claude Sonnet)")}

<!-- 시그널 유형 분포 -->
<div style="background:#fff;border-radius:12px;padding:18px 22px;
            box-shadow:0 2px 8px rgba(0,0,0,.06);margin:16px 0">
  <div style="font-size:13px;font-weight:700;color:#1a1a2e;margin-bottom:14px">
    📈 시그널 유형 분포 (30일)
  </div>
  {type_bars}
</div>

<!-- 회사별 월간 현황 테이블 -->
<div style="font-size:13px;font-weight:700;color:#1a1a2e;margin:20px 0 8px">
  🏢 회사별 월간 현황
</div>
<table style="width:100%;border-collapse:collapse;background:#fff;
              border-radius:10px;overflow:hidden;box-shadow:0 2px 8px rgba(0,0,0,.06)">
  <thead>
    <tr style="background:#1a1a2e">
      <th style="padding:10px 16px;text-align:left;font-size:11px;color:#adb5bd;font-weight:600">회사</th>
      <th style="padding:10px;text-align:center;font-size:11px;color:#e74c3c;font-weight:600">🔴</th>
      <th style="padding:10px;text-align:center;font-size:11px;color:#f39c12;font-weight:600">🟡</th>
      <th style="padding:10px;text-align:center;font-size:11px;color:#adb5bd;font-weight:600">⚪</th>
      <th style="padding:10px;text-align:left;font-size:11px;color:#adb5bd;font-weight:600">주요 유형</th>
      <th style="padding:10px 16px;text-align:left;font-size:11px;color:#adb5bd;font-weight:600">최근 이슈</th>
    </tr>
  </thead>
  <tbody>{co_rows}</tbody>
</table>"""


