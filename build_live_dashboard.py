def _flag_badge(flag: str) -> str:
    cfg = {"red": ("#fdf0f0","#e74c3c","🔴 즉시검토"),
           "yellow": ("#fffbf0","#f39c12","🟡 모니터링"),
           "white": ("#f8f9fa","#adb5bd","⚪ 참고")}
    bg, color, label = cfg.get(flag, ("#f8f9fa","#adb5bd","⚪"))
    return (f'<span style="background:{bg};color:{color};font-size:11px;'
            f'font-weight:700;padding:3px 10px;border-radius:12px">{label}</span>')


def _ai_insight_box(text: str, title: str = "🤖 AI 인사이트 (Claude Sonnet)") -> str:
    if not text:
        return ""
    text_html = text.replace("\n", "<br>")
    return f"""
<div style="background:linear-gradient(135deg,#f8f0ff,#fff);
            border:1.5px solid #c39bd3;border-radius:12px;
            padding:18px 22px;margin:18px 0">
  <div style="font-size:11px;font-weight:700;color:#8e44ad;
              letter-spacing:.8px;text-transform:uppercase;margin-bottom:10px">
    {title}
  </div>
  <div style="font-size:13.5px;line-height:1.9;color:#2c3e50">{text_html}</div>
  <div style="font-size:10px;color:#bdc3c7;text-align:right;margin-top:8px">
    Generated by Claude Sonnet · AI 분석 결과이므로 반드시 검토 후 사용
  </div>
</div>"""


