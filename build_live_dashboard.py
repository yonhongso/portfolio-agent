def main(output_path: str = None):
    from dotenv import load_dotenv
    load_dotenv()

    if output_path is None:
        output_path = os.path.join(os.path.dirname(__file__), "dashboard.html")

    db = SignalDB()
    daily_signals   = db.get_recent(days=1)
    weekly_signals  = db.get_weekly()
    monthly_signals = db.get_monthly()

    logger.info(f"[LiveDashboard] Daily:{len(daily_signals)} / Weekly:{len(weekly_signals)} / Monthly:{len(monthly_signals)}")

    weekly_ai  = _generate_weekly_insight(weekly_signals)  if weekly_signals  else ""
    monthly_ai = _generate_monthly_insight(monthly_signals, datetime.now().year,
                                           datetime.now().month) if monthly_signals else ""

    html = build_live_dashboard(
        daily_signals, weekly_signals, monthly_signals,
        weekly_ai=weekly_ai, monthly_ai=monthly_ai,
    )
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(html)
    logger.info(f"[LiveDashboard] 저장 완료 → {output_path}")
    return output_path


